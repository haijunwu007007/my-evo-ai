BASE = "http://127.0.0.1:8765"

def test_api(msg, timeout=60):
    body = json.dumps({"message": msg, "lang": "zh-CN"}).encode("utf-8")
    req = urllib.request.Request(f"{BASE}/api/v1/smart", data=body,
        headers={"Content-Type": "application/json"}, method="POST")
    try:
        resp = urllib.request.urlopen(req, timeout=timeout)
        data = json.loads(resp.read().decode("utf-8"))
        return data.get("success"), data.get("mode","?"), str(data.get("result",""))[:100]
    except Exception as e:
        return False, "error", str(e)[:100]

