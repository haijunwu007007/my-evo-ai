def run_batch(batch_id, cases):
    print(f"\n{'='*60}")
    print(f"  批次 {batch_id}: {len(cases)} 项测试")
    print(f"{'='*60}")
    ok=fail=0; details=[]
    for i,(ind,dim,msg) in enumerate(cases,1):
        s,mo,re=test_api(msg)
        r='✅' if s else '❌'
        if s: ok+=1
        else: fail+=1
        details.append((r,ind,dim,re))
        print(f"  [{i}/{len(cases)}] {r} {ind[:4]} {dim} | {re[:50]}")
        time.sleep(0.3)
    print(f"\n  本批: ✅{ok} ❌{fail}")
    return ok, fail, details

if __name__ == "__main__":
    total_ok=total_fail=0
    all_details=[]
    os.makedirs("test_output", exist_ok=True)
    batch_size=50
    for bidx in range(0, len(CASES), batch_size):
        batch=CASES[bidx:bidx+batch_size]
        ok,fail,details=run_batch(bidx//batch_size+1, batch)
        total_ok+=ok; total_fail+=fail; all_details+=details
        # 保存中间结果
        with open(f"test_output/batch_{bidx//batch_size+1}.json","w",encoding="utf-8") as f:
            json.dump({"batch":bidx//batch_size+1,"ok":ok,"fail":fail,"details":details},f,ensure_ascii=False)
    # 总报告
    total = total_ok + total_fail
    print(f"\n{'='*60}")
    print(f"  📊 能力矩阵测试报告")
    print(f"  {time.strftime('%Y-%m-%d %H:%M:%S')}")
    print(f"{'='*60}")
    print(f"  总分: ✅{total_ok} ❌{total_fail} 总计{total}")
    print(f"  通过率: {total_ok*100//total if total else 0}%")
    print()
    for dim in DIMENSIONS:
        d_ok=sum(1 for r,_,d,_ in all_details if r=='✅' and d==dim)
        d_total=sum(1 for _,_,d,_ in all_details if d==dim)
        print(f"  {dim}: {d_ok}/{d_total} ", end="")
        if d_ok==d_total: print("✅")
        elif d_ok>=d_total*0.8: print("👍")
