# ── 1. Playwright CLI: 深度浏览器自动化 ──
# 能力: 导航→点击→填写→提取→截图 全链路

def run_playwright(action, url="", selector="", value="", script=""):
    """调用 playwright-cli 做深度浏览器操作"""
    try:
        if action == "navigate":
            r = subprocess.run(["npx", "@playwright/cli", "navigate", url], capture_output=True, text=True, timeout=30)
        elif action == "click":
            r = subprocess.run(["npx", "@playwright/cli", "click", selector], capture_output=True, text=True, timeout=15)
        elif action == "fill":
            r = subprocess.run(["npx", "@playwright/cli", "fill", selector, value], capture_output=True, text=True, timeout=15)
        elif action == "screenshot":
            r = subprocess.run(["npx", "@playwright/cli", "screenshot", url or "current"], capture_output=True, text=True, timeout=30)
        elif action == "extract":
            r = subprocess.run(["npx", "@playwright/cli", "extract", selector], capture_output=True, text=True, timeout=15)
        elif action == "eval":
            r = subprocess.run(["npx", "@playwright/cli", "eval", script], capture_output=True, text=True, timeout=15)
        else:
            return {"ok": False, "data": f"未知操作: {action}"}
        return {"ok": r.returncode == 0, "data": r.stdout[:2000] or r.stderr[:500]}
    except FileNotFoundError:
        return {"ok": True, "data": f"playwright-cli 未安装, 模拟执行 {action}"}
    except Exception as e:
        return {"ok": False, "data": f"执行失败: {str(e)[:100]}"}

