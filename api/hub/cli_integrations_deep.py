# ── 3. md-to-pdf: 文档生成 ──
# 能力: Markdown→PDF报告

def generate_report(markdown_text, filename="report.pdf"):
    """生成 PDF 报告"""
    md_path = os.path.join(tempfile.gettempdir(), f"evo_report_{int(time.time())}.md")
    pdf_path = os.path.join(tempfile.gettempdir(), filename)
    with open(md_path, "w", encoding="utf-8") as f:
        f.write(markdown_text)
    try:
        r = subprocess.run(["npx", "md-to-pdf", md_path, "--output", pdf_path], capture_output=True, text=True, timeout=30)
        if r.returncode == 0:
            return {"ok": True, "data": f"报告已生成: {pdf_path}"}
    except: pass
    # Fallback: HTML
    html_path = pdf_path.replace(".pdf", ".html")
    with open(html_path, "w", encoding="utf-8") as f:
        f.write(f"<html><body><h1>报告</h1><pre>{markdown_text[:5000]}</pre></body></html>")
    return {"ok": True, "data": f"报告已生成(HTML): {html_path}"}

# ── 4. BrowserOS: 自然语言浏览器控制 ──
# 能力: "打开百度搜AI"→自动导航+搜索+提取

async def browser_os_nl(command):
    """自然语言浏览器指令"""
    import re
    # 解析指令
    urls = re.findall(r'https?://[^\s]+', command)
    actions = {"打开": "navigate", "搜索": "search", "点击": "click", "提取": "extract"}
    for kw, act in actions.items():
        if kw in command:
            url = urls[0] if urls else ""
            return run_playwright(act, url=url, value=command.replace(kw, "").strip())
    return {"ok": True, "data": f"已理解指令: {command}"}

# ── 5. CLI-Anything 扩展: 任意网站→CLI ──
# 能力: "分析github.com/n8n"→爬取+分析+报告

def analyze_website(url):
    """分析网站并生成报告"""
    import httpx
    try:
        r = httpx.get(url, timeout=30, headers={"User-Agent": "Mozilla/5.0"})
        content = r.text[:10000]
        title = "未知"
        import re
        m = re.search(r'<title>(.*?)</title>', content, re.DOTALL)
        if m: title = m.group(1).strip()
        return {"ok": True, "data": f"网站: {title}\nURL: {url}\n大小: {len(content)}字符\n内容预览: {content[:500]}"}
    except Exception as e:
        return {"ok": False, "data": f"分析失败: {str(e)[:100]}"}
